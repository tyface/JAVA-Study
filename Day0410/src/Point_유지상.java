
public class Point_유지상 {
	// 2차원 좌표값을 저장하는 클래스
	// x, y
	// get,set,toString
	private int x,y;

	
	
	@Override
	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
