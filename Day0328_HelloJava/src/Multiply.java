
public class Multiply { // 클래스

	public static void main(String[] args) { // 메인메서드
		int a; // 변수선언, 'a' 라는 이름의 정수형 변수 선언
		int b; // 변수선언, 'b' 라는 이름의 정수형 변수 선언
		int result; // 변수선언, 'mtp' 라는 이름의 정수형 변수 선언

		a = 50; // a라는 변수에 50을 넣어줌
		b = 100; // b라는 변수에 100을 넣어줌
		result = a * b; // mtp라는 변수에 a곱하기 b의 값을 넣어줌

		System.out.print("a곱하기 b는 "); // ""안의 내용을 콘솔에 출력
		System.out.print(result); // mtp이라는 변수가 가지는 값을 콘솔에 출력
		System.out.print("입니다."); // ""안의 내용을 콘솔에 출력

	}
}
