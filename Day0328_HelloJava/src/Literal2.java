
public class Literal2 {

	public static void main(String[] args) {
		// 실수형 상수를 표현하는 방법에 대해서 실습합니다.
		// 자바에서의 실수 기본자료형은 double!!
		double r1 = 123.5;
		float r2 = 123.5f;
		// 자바에서는 지수표기법을 이용한 실수를 표현할 수 있다.
		
		float r3 = 1.235e2f;
		float r4 = 1.235e-2f;
		System.out.println(r4);
		
		char str = 'a';
		boolean isTrue = false;
		
	}

}
